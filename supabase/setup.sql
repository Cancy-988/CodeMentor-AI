  exists (
    select 1 from public.users u where u.id = projects.user_id and u.supabase_user_id = auth.uid()::text
  )
)
with check (
  exists (
    select 1 from public.users u where u.id = projects.user_id and u.supabase_user_id = auth.uid()::text
  )
);

-- Fixes table and RLS
