create table if not exists public.users (
  id integer generated by default as identity primary key,
  supabase_user_id varchar(128) not null unique,
  email varchar(320),
  full_name varchar(255),
  avatar_url text,
  auth_provider varchar(64) not null default 'google',
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create index if not exists ix_users_supabase_user_id on public.users (supabase_user_id);

create table if not exists public.chats (
  id integer generated by default as identity primary key,
  user_id integer not null references public.users(id) on delete cascade,
  title varchar(255) not null,
  summary text,
  language varchar(50),
  framework varchar(100),
  source varchar(50) not null default 'chat',
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create index if not exists ix_chats_user_id on public.chats (user_id);

create table if not exists public.messages (
  id integer generated by default as identity primary key,
  chat_id integer not null references public.chats(id) on delete cascade,
  role varchar(20) not null,
  content text not null,
  metadata_json jsonb,
  created_at timestamptz not null default now()
);

create index if not exists ix_messages_chat_id on public.messages (chat_id);

create table if not exists public.reviews (
  id integer generated by default as identity primary key,
  user_id integer not null references public.users(id) on delete cascade,
  chat_id integer references public.chats(id) on delete set null,
  language varchar(50) not null,
  code text not null,
  result_json jsonb not null,
  created_at timestamptz not null default now()
);

create index if not exists ix_reviews_user_id on public.reviews (user_id);
create index if not exists ix_reviews_chat_id on public.reviews (chat_id);

create table if not exists public.uploads (
  id integer generated by default as identity primary key,
  user_id integer not null references public.users(id) on delete cascade,
  chat_id integer references public.chats(id) on delete set null,
  filename varchar(255) not null,
  language varchar(50) not null,
  content text not null,
  file_type varchar(50) not null default 'code',
  mime_type varchar(100),
  storage_path text,
  extracted_text text,
  created_at timestamptz not null default now()
);

create index if not exists ix_uploads_user_id on public.uploads (user_id);
create index if not exists ix_uploads_chat_id on public.uploads (chat_id);

create table if not exists public.memories (
  id integer generated by default as identity primary key,
  user_id integer not null references public.users(id) on delete cascade,
  key varchar(120) not null,
  value_json jsonb not null,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now(),
  constraint uq_memory_user_key unique (user_id, key)
);

create index if not exists ix_memories_user_id on public.memories (user_id);

alter table public.users enable row level security;
alter table public.chats enable row level security;
alter table public.messages enable row level security;
alter table public.reviews enable row level security;
alter table public.uploads enable row level security;
alter table public.memories enable row level security;

