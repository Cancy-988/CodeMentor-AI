insert into storage.buckets (id, name, public)
values
  ('code-uploads', 'code-uploads', false),
  ('screenshots', 'screenshots', false),
  ('attachments', 'attachments', false)
on conflict (id) do update
set name = excluded.name,
    public = excluded.public;

drop policy if exists "Users can manage their own storage objects" on storage.objects;
create policy "Users can manage their own storage objects"
on storage.objects
for all
using (
  bucket_id in ('code-uploads', 'screenshots', 'attachments')
  and auth.uid()::text = split_part(name, '/', 1)
)
with check (
  bucket_id in ('code-uploads', 'screenshots', 'attachments')
  and auth.uid()::text = split_part(name, '/', 1)
);

-- Projects table and RLS
create table if not exists public.projects (
  id integer generated by default as identity primary key,
  user_id integer not null references public.users(id) on delete cascade,
  name varchar(255) not null,
  description text,
  language varchar(50),
  framework varchar(100),
  status varchar(50),
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create index if not exists ix_projects_user_id on public.projects (user_id);

